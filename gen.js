import kix from './jay';
import gn from './qry/gn';
import { fromJS } from 'immutable';

const k = new kix();

export default () => 
    fromJS(gn()(k.u('+++++++ ++  +++++ +++ ++++++++')))
        .set(k.u('+++++++  +  ++ +++++++  ++ ++  ++  ++++++ ++++++++ ++  +++ ++++'), 
            fromJS(gn()(k.u('+++++++ ++  +++++ +++ ++++++++')))
                .set(k.u('+  +++ ++++  +++++++ +++++  +++++++ +++ +++++++++'), 
                    '+++++++  ++++++ ++++++++ ++++  + ++ +++++++  _  +++++++ ++++++++  +++++++  ++++++  +++++++  '))
        .toJS()
